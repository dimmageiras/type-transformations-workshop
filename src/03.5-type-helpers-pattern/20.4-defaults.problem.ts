import { Equal, Expect } from "@total-typescript/helpers";

type CreateDataShape<
  TData extends string | boolean,
  TError extends Error | undefined = undefined,
> = {
  data: TData;
  error: TError;
};

type tests = [
  Expect<
    Equal<
      CreateDataShape<string>,
      {
        data: string;
        error: undefined;
      }
    >
  >,
  Expect<
    Equal<
      CreateDataShape<boolean, SyntaxError>,
      {
        data: boolean;
        error: SyntaxError;
      }
    >
  >,
];
